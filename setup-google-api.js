#!/usr/bin/env node
/**
 * Setup Google Sheets API Key
 * Prompts user for API Key and saves to .env file
 */

const fs = require('fs');
const path = require('path');
const readline = require('readline');

// Save configuration under Bordero/config relative to repository root
const CONFIG_DIR = path.join(__dirname, 'Bordero', 'config');
const ENV_FILE = path.join(CONFIG_DIR, '.env');

// Create config directory if it doesn't exist
if (!fs.existsSync(CONFIG_DIR)) {
  fs.mkdirSync(CONFIG_DIR, { recursive: true });
}

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

console.log('');
console.log('╔════════════════════════════════════════════════════════════════╗');
console.log('║       Google Sheets API - Setup della Chiave API               ║');
console.log('╚════════════════════════════════════════════════════════════════╝');
console.log('');
console.log('📖 Se non hai ancora la API Key, segui i passaggi in:');
console.log('   Bordero/GOOGLE_SHEETS_API_SETUP.md');
console.log('');

rl.question('📝 Incolla la tua API Key di Google Cloud: ', (apiKey) => {
  const trimmedKey = apiKey.trim();

  if (!trimmedKey) {
    console.log('❌ Errore: API Key vuota');
    rl.close();
    process.exit(1);
  }

  if (trimmedKey.length < 20) {
    console.log('⚠️  Avvertimento: la chiave sembra troppo corta');
  }

  const envContent = `# Google Sheets API Configuration
# Generated by setup-google-api.js

GOOGLE_API_KEY=${trimmedKey}

# Sheet IDs
GOOGLE_SHEET_BRANI=1vWgzzwSHL2hK9RGvP87sCy_AtGGNTcDRM-h85bIpMMM
GOOGLE_SHEET_COMUNI=1235O_kj7vNHHpm7g24-n6WLsrdySBY3lls6s-h1l1L0
GOOGLE_SHEET_DBASE=137GoBHK098j4-4YwE4b4jhAmkcR_zV7u5Er75hR3Amc

# Sheet GIDs (sheet index within spreadsheet, 0 = first sheet)
GOOGLE_SHEET_BRANI_GID=0
GOOGLE_SHEET_COMUNI_GID=0
GOOGLE_SHEET_DBASE_GID=1174825842

# Output directory for CSV files
OUTPUT_DIR=../data
`;

  try {
    if (!fs.existsSync(CONFIG_DIR)) {
      fs.mkdirSync(CONFIG_DIR, { recursive: true });
    }
    fs.writeFileSync(ENV_FILE, envContent);
    console.log('');
    console.log('✅ Configurazione salvata in: Bordero/config/.env');
    console.log('');
    console.log('🚀 Prossimo passo:');
    console.log('   cd Bordero/server');
    console.log('   npm install');
    console.log('   node google-sheets-sync.js');
    console.log('');
  } catch (error) {
    console.log('❌ Errore nel salvataggio: ' + error.message);
    rl.close();
    process.exit(1);
  }

  rl.close();
});
